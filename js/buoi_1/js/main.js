// ES5 (Phiên bản của Javascript)
//Đặt tên theo camel case (viết in hoa chữ cái đầu tiên của cụm từ thứ 2 trở đi)
var fullName = 'Nguyễn Văn A';


//Kiểm tra kết quả
//Sử dụng biến
